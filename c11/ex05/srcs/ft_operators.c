#include "../includes/ft.h"

int	ft_addition(int a, int b)
{
	return (a + b);
}

int	ft_soustraction(int a, int b)
{
	return (a - b);
}

int	ft_multiplication(int a, int b)
{
	return (a * b);
}

int	ft_division(int a, int b)
{
	return (a / b);
}

int	ft_modulo(int a, int b)
{
	return (a % b);
}
